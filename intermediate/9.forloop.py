## using while loop
data = [2, 'bijay', 4.5, 8, 'python', 'java']

i = 0

while i < len(data):
    print(data[i])
    i += 1



## using for loop

dlist = [12, 'pandey', 4.5, 8, 'js', 'cpp']

for i in range(len(dlist)):
    print(dlist[i])


## we can also use like this
data = [2, 'bijay', 4.5, 8, 'python', 'java']

